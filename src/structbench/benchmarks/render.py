"""Render benchmark cards to human-facing views (ADR-0027).

The card instances in each benchmark module are the source of truth;
``docs/benchmarks.md`` and the per-archive README/``card.json`` are
generated from them by ``tools/gen_benchmark_docs.py``. A drift test
asserts the committed index matches :func:`render_index`.
"""

from __future__ import annotations

import json

from .card import BenchmarkCard
from .registry import BenchmarkSpec
from .results import BaselineResult

_MARKER = "<!-- generated by tools/gen_benchmark_docs.py; do not edit by hand -->"


def _yesno(flag: bool) -> str:
    return "yes" if flag else "no"


def _fmt_value(value: float) -> str:
    return f"{value:g}"


def _registry_name(spec: BenchmarkSpec) -> str | None:
    """The registry key owning ``spec``, matched by unique card name.

    Landing pages live at ``docs/benchmarks/<registry-name>.md``; the index
    links to them. Matching by ``card.name`` (unique per benchmark) keeps
    working for ``dataclasses.replace``-d specs used in tests, which no longer
    share object identity with the registry's ``SPEC``.
    """
    from .registry import available_benchmarks, get_benchmark

    for name in available_benchmarks():
        if get_benchmark(name).card.name == spec.card.name:
            return name
    return None


def _page_asset(path: str) -> str:
    """Rewrite a repo-relative asset path for a page at ``docs/benchmarks/``.

    A committed figure path like ``assets/foo.png`` becomes ``../../assets/foo.png``
    so the link resolves from the two-deep page location; absolute URLs pass
    through unchanged.
    """
    if path.startswith(("http://", "https://")):
        return path
    return f"../../{path}"


def _task_lines(c: BenchmarkCard) -> list[str]:
    """The shared 'Task' section (archive README and landing page)."""
    return [
        "## Task",
        "",
        f"{c.task}. Auxiliary target: `{c.aux_field}` ({c.aux_unit}). "
        "Models advance the particle state autoregressively from a short "
        "ground-truth prefix and are scored on the full predicted rollout.",
    ]


def _eval_lines(c: BenchmarkCard, *, fold_rationale: bool = False) -> list[str]:
    """The shared 'Evaluation criteria' section (archive README and page).

    ``fold_rationale`` moves the long ``protocol_rationale`` into a collapsed
    ``<details>`` block after the metrics (used by the landing page, where the
    full paragraph would otherwise dominate the section) instead of an inline
    bullet. The text is rendered verbatim either way — single source, no
    rewrite.
    """
    lines = [
        "## Evaluation criteria",
        "",
        f"- Protocol (benchmark-owned, ADR-0032, ADR-0035): {c.input_frames} "
        f"input frames, horizon {c.horizon}, scored at {c.eval_times} output times.",
    ]
    if not fold_rationale:
        lines.append(f"- Protocol rationale: {c.protocol_rationale}")
    lines += [
        f"- Metrics: one-step and full-rollout position RMSE (mm); "
        f"{c.aux_field} RMSE ({c.aux_unit}).",
        f"- Quantities of interest: {', '.join(c.qois)}.",
    ]
    if fold_rationale:
        lines += [
            "",
            "<details>",
            "<summary>Protocol rationale — the ground-truth timeline analysis "
            "behind these values (ADR-0032 §5)</summary>",
            "",
            c.protocol_rationale,
            "",
            "</details>",
        ]
    return lines


def _baseline_line(spec: BenchmarkSpec) -> str:
    """Compact one-line results summary for the docs index (ADR-0033)."""
    if not spec.results:
        return "*no official baseline yet*"
    parts = []
    for r in spec.results:
        headline = ""
        for split in spec.splits:
            if split in r.metrics:
                metric, value = next(iter(r.metrics[split].items()))
                headline = f": {split} {metric} {_fmt_value(value)}"
                break
        parts.append(
            f"{r.label} ({r.family}, {r.run_date}, `{r.run_commit}`){headline}"
        )
    return "; ".join(parts)


def _metric_table(
    spec: BenchmarkSpec, r: BaselineResult, metric_names: list[str]
) -> list[str]:
    """One markdown table: split rows (card order) x the given metric columns."""
    lines = [
        "| split | " + " | ".join(metric_names) + " |",
        "|---|" + "---|" * len(metric_names),
    ]
    for split in spec.splits:
        if split not in r.metrics:
            continue
        values = r.metrics[split]
        cells = [_fmt_value(values[m]) if m in values else "—" for m in metric_names]
        lines.append(f"| {split} | " + " | ".join(cells) + " |")
    return lines


def _numbers_to_beat(spec: BenchmarkSpec) -> list[str]:
    """The results section: per blessed result, a trajectory-error table and,
    when present, a separate quantities-of-interest table (``qoi_*`` metrics).

    Splitting keeps each table narrow; a result with only one metric group
    renders a single unlabelled table (unchanged behaviour).
    """
    lines = ["## Numbers to beat", ""]
    if not spec.results:
        lines.append(
            "*No official baseline yet — the reference run's metrics land here.*"
        )
        return lines
    for r in spec.results:
        suffix = f", checkpoint: {r.checkpoint}" if r.checkpoint else ""
        lines.append(
            f"**{r.label}** ({r.family}, {r.run_date}, commit `{r.run_commit}`{suffix})"
        )
        lines.append("")
        # Metric columns in first-seen order across splits (card order).
        metric_names: list[str] = []
        for split in spec.splits:
            for metric in r.metrics.get(split, {}):
                if metric not in metric_names:
                    metric_names.append(metric)
        qoi = [m for m in metric_names if m.startswith("qoi_")]
        rmse = [m for m in metric_names if not m.startswith("qoi_")]
        if qoi and rmse:
            groups = [
                ("Trajectory error (RMSE)", rmse),
                ("Quantities of interest (MAE)", qoi),
            ]
        else:
            groups = [("", metric_names)]
        for title, names in groups:
            if title:
                lines.extend([f"_{title}_", ""])
            lines.extend(_metric_table(spec, r, names))
            lines.append("")
        lines.pop()  # drop the trailing blank of the last group
        if r.notes:
            lines.extend(["", f"*{r.notes}*"])
        lines.append("")
    return lines[:-1]


def render_index(specs: list[BenchmarkSpec]) -> str:
    """The full ``docs/benchmarks.md`` content for the given specs.

    Parameters
    ----------
    specs : list of BenchmarkSpec
        Ordered list of benchmarks to render as a comparison table.

    Returns
    -------
    str
        Markdown document containing the generation marker, a 9-column
        comparison table, and one ## section per benchmark.
    """
    lines = [
        _MARKER,
        "",
        "# Benchmarks",
        "",
        "One row per benchmark; see each section for the full card.",
        "Note: `sph/stress` and `sph/strain` are full 6-component Voigt "
        "tensors; each benchmark's scalar target (von Mises, axial, principal) "
        "is derived from them by the loader — see the Aux target column.",
        "",
        "| Benchmark | Solver | Discretisation | Erosion | Loading | Cases "
        "| Particles | Frames | Aux target |",
        "|---|---|---|---|---|---|---|---|---|",
    ]
    for spec in specs:
        c = spec.card
        lines.append(
            f"| {c.name} | {c.solver} | {c.discretisation} | {_yesno(c.erosion)} "
            f"| {c.loading} | {c.n_cases} | {c.particles_per_case} "
            f"| {c.n_frames} | {c.aux_field} ({c.aux_unit}) |"
        )
    for spec in specs:
        lines.extend(["", *_section(spec)])
    return "\n".join(lines) + "\n"


def _section(spec: BenchmarkSpec) -> list[str]:
    c = spec.card
    splits = ", ".join(f"{name} {n}" for name, n in c.splits.items())
    name = _registry_name(spec)
    lines = [
        f"## {c.name} (v{c.version})",
        "",
        c.description,
        "",
        f"- **Task**: {c.task}",
        f"- **Materials**: {', '.join(c.materials)}",
        f"- **Geometry**: {c.geometry}; source units {c.source_units}",
        f"- **Splits**: {splits}",
        f"- **Protocol** (ADR-0032, ADR-0035): {c.input_frames} input frames, "
        f"horizon {c.horizon}, scored at {c.eval_times} output times. "
        f"*Rationale*: {c.protocol_rationale}",
        f"- **QoIs**: {', '.join(c.qois)}",
        f"- **Baseline**: {_baseline_line(spec)}",
        f"- **Fields**: {', '.join(c.fields)}",
        f"- **Provenance**: {c.provenance}",
        f"- **License**: {c.data_license}",
    ]
    if name is not None:
        lines.append(
            f"- **Full page**: [docs/benchmarks/{name}.md](benchmarks/{name}.md)"
        )
    return lines


def render_archive_readme(spec: BenchmarkSpec, name: str) -> str:
    """A standalone README for the hosted dataset archive.

    Parameters
    ----------
    spec : BenchmarkSpec
        The benchmark specification whose card describes the hosted dataset.
    name : str
        The registry name (archive folder name), e.g. ``"taylor_impact_2d"``
        — used for the grouped-config path in the usage section.

    Returns
    -------
    str
        Standalone README markdown for the dataset archive.
    """
    c = spec.card
    splits_str = ", ".join(f"{k} {v}" for k, v in c.splits.items())
    lines = [
        f"# {c.name} — StructBench canonical dataset",
        "",
        c.description,
        "",
        f"- Solver: {c.solver} ({c.discretisation}; erosion: {_yesno(c.erosion)})",
        f"- Loading: {c.loading}",
        f"- Source units: {c.source_units} (files are strict SI, ADR-0012)",
        f"- Cases: {c.n_cases} ({splits_str})",
        f"- Particles per case: {c.particles_per_case}; "
        f"{c.n_frames} frames at {c.output_dt_ms} ms",
        f"- Provenance: {c.provenance}",
        f"- License: {c.data_license}",
        "",
        *_task_lines(c),
        "",
        *_eval_lines(c),
        "",
        *_numbers_to_beat(spec),
        "",
        "## Using this archive",
        "",
        "```bash",
        "pip install structbench  # or: pip install -e . from the repo",
        f"structbench-train --mode train --config configs/{name}/cgn.toml \\",
        f"    --data-root /path/to/this/folder --out runs/{name}-cgn",
        "```",
        "",
        "Machine-readable metadata: `card.json` alongside this file.",
        "`sph/stress` and `sph/strain` are 6-component Voigt tensors; "
        "scalar targets are loader-derived (see the card's aux field).",
        "Consume with `structbench.datasets.load_case_trajectory` or any "
        "HDF5 reader (layout per ADR-0013).",
    ]
    return "\n".join(lines) + "\n"


def render_benchmark_page(spec: BenchmarkSpec, name: str) -> str:
    """The committed, browsable landing page for one benchmark (ADR-0036).

    Reuses the archive README's Task / Evaluation / Numbers-to-beat bodies —
    so the numbers keep a single source — and adds the card's non-derivable
    ``overview`` narrative and ``figures``, plus a quickstart. Generated to
    ``docs/benchmarks/<name>.md``; a drift test asserts the committed page
    matches this render.

    Parameters
    ----------
    spec : BenchmarkSpec
        The benchmark to render.
    name : str
        Its registry name (page filename and grouped-config path).

    Returns
    -------
    str
        Markdown for the landing page, with the generation marker.
    """
    c = spec.card
    splits_str = ", ".join(f"{k} {v}" for k, v in c.splits.items())
    size = f"; {c.size_gb} GB on disk" if c.size_gb else ""
    lines = [_MARKER, "", f"# {c.name} — StructBench benchmark", ""]
    if c.overview.strip():
        lines += [c.overview.strip(), ""]
    if c.figures:
        lines += ["## Figures", ""]
        for fig in c.figures:
            lines += [
                f"![{fig.alt or fig.caption}]({_page_asset(fig.path)})",
                "",
                f"*{fig.caption}*",
                "",
            ]
    lines += [
        "## Data at a glance",
        "",
        f"- Solver: {c.solver} ({c.discretisation}; erosion: {_yesno(c.erosion)})",
        f"- Loading: {c.loading}",
        f"- Geometry: {c.geometry}",
        f"- Source units: {c.source_units} (canonical storage is strict SI, ADR-0012)",
        f"- Cases: {c.n_cases} ({splits_str})",
        f"- Particles per case: {c.particles_per_case}; "
        f"{c.n_frames} frames at {c.output_dt_ms} ms{size}",
        f"- Fields: {', '.join(c.fields)}",
        f"- Provenance: {c.provenance}",
        f"- License: {c.data_license}",
        "",
        *_task_lines(c),
        "",
        *_eval_lines(c, fold_rationale=True),
        "",
        *_numbers_to_beat(spec),
        "",
        "## Quickstart",
        "",
        "```bash",
        "pip install structbench  # or: pip install -e . from the repo",
        f"structbench-train --mode train --config configs/{name}/cgn.toml \\",
        f"    --data-root /path/to/{name} --out runs/{name}-cgn",
        "```",
        "",
        "Dataset download and hosting: see the repository README. The "
        "cross-benchmark index is [docs/benchmarks.md](../benchmarks.md); "
        "machine-readable card metadata ships as `card.json` with the data archive.",
    ]
    return "\n".join(lines) + "\n"


def card_json(card: BenchmarkCard) -> str:
    """The card as pretty-printed JSON for the dataset archive.

    Parameters
    ----------
    card : BenchmarkCard
        The benchmark card to serialize.

    Returns
    -------
    str
        Pretty-printed JSON with 2-space indentation and a trailing newline.
    """
    return json.dumps(card.to_json_dict(), indent=2) + "\n"
